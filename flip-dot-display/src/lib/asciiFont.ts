export function textToMatrix(text: string, cols: number, rows: number): boolean[][] {
  const matrix: boolean[][] = Array(rows).fill(null).map(() => Array(cols).fill(false));

  const lines = text.split('\n');
  let currentRow = 0;

  for (const line of lines) {
    if (currentRow >= rows) break;

    let currentCol = 0;
    for (const char of line) {
      const pattern = ASCII_FONT[char] || ASCII_FONT[' '];

      // Check if we can fit the character
      if (currentCol + 5 > cols) {
        break;
      }

      // Place the character
      for (let r = 0; r < 7 && currentRow + r < rows; r++) {
        for (let c = 0; c < 5 && currentCol + c < cols; c++) {
          matrix[currentRow + r][currentCol + c] = pattern[r][c] === 1;
        }
      }

      currentCol += 6; // 5 for character + 1 for spacing
    }

    currentRow += 8; // 7 for character height + 1 for line spacing
  }

  return matrix;
}

export function asciiArtToMatrix(art: string, cols: number, rows: number): boolean[][] {
  const matrix: boolean[][] = Array(rows).fill(null).map(() => Array(cols).fill(false));
  const lines = art.split('\n');

  for (let r = 0; r < Math.min(lines.length, rows); r++) {
    const line = lines[r];
    for (let c = 0; c < Math.min(line.length, cols); c++) {
      // Any non-space character becomes a lit dot
      matrix[r][c] = line[c] !== ' ' && line[c] !== '';
    }
  }

  return matrix;
}